								<p></p><div class="jsn-bootstrap3"><div id="nc3L0G" class=" row"><div class="col-md-6 col-sm-6 col-xs-12"><div data-scroll-reveal="enter left and move 150px over 0.9s" style="-webkit-transform: translatex(0);transform: translatex(0);opacity: 1;-webkit-transition: -webkit-transform 0.9s ease-in-out 0s,  opacity 0.9s ease-in-out 0s;transition: transform 0.9s ease-in-out 0s, opacity 0.9s ease-in-out 0s;-webkit-perspective: 1000;-webkit-backface-visibility: hidden;" data-scroll-reveal-initialized="true"><div class="jsn-bootstrap3 jsn-pagebuilder pb-element-container pb-element-image"><img src="deyinsekcijatxt.png" alt=""></div></div></div><div class="col-md-6 col-sm-6 col-xs-12"><div class="pb-element-container pb-element-text text-gexclw"><div>
<p style="text-align: justify;">Dezinsekcijom se smatraju postupci suzbijanja artropoda (člankonožaca) koji mogu prenijeti uzročnike zaraznih bolesti, izazvati alergijske reakcije, imati toksično djelovanje ili su nametnici odnosno uznemirivači.</p>
<p style="text-align: justify;">Insekti su najveći životinjski razred kako po broju vrsta, tako i po broju individua. Taj golemi broj vrsta prilagodio se različitim ekološkim odnosima i učestvuje u najraznovrsnijim procesima u prirodi. U ekološku vezu sa čovjekom insekti ulaze kao direktni ili indirektni prenosnici zaraznih bolesti, kao paraziti ili kao štetnici koji uništavaju ili oštećuju čovjeku korisne resurse. &nbsp;</p>
<p style="text-align: justify;">Dezinsekcija je sistem objedinjenih preventivnih i korektivnih postupaka kojima je cilj nadziranje populacije insekata kako ne bi postali problem, odnosno da ne smetaju, ugrožavaju zdravlje ili nanose ekonomske štete. &nbsp; U provođenju dezinsekcije koristimo se:&nbsp; biološkim, fizikalno mehaničkim ili hemijskim metodama. &nbsp;</p>
<p style="text-align: justify;">Preventivna dezinsekcija kao obavezna zakonska mjera sanitarne zaštite isključuje svaku eventualnu jaču infestaciju objekata štetočinama, a samim tim i sve negativne posljedice.&nbsp;</p>
<p style="text-align: justify;">Dezinsekcija podrazumijeva i način sprječavanja ulaženja i zadržavanja štetnih člankonožaca (Arthropoda) na površine, u prostor ili objekat.</p>
<p style="text-align: justify;">Štetnici od javnozdravstvene važnosti mogu se podjeliti u više grupa i to:</p>
<ul style="text-align: justify;">
<li style="text-align: justify;">-&nbsp; Prijenosnici zaraznih bolesti: <em><strong>komarci, muhe, buhe, uši tijela, krpelji</strong></em>;</li>
<li style="text-align: justify;">- Mehanički prijenosnici mikroorganizama:<em><strong> žohari (smeđi i crni), mravi (faraonski i kućni);</strong></em></li>
<li style="text-align: justify;">- Paraziti na tijelu: <em><strong>uši glave i stidne uši</strong></em>;</li>
<li style="text-align: justify;">- Uzročnici alergijskih reakcija: <em><strong>stjenice, leptiri, kornjaši, grinje</strong></em>;</li>
<li style="text-align: justify;">- Štetni člankonošci toksičnog djelovanja:<em><strong> pauci, tarantula, ose, stršljen, obad, štipavci, stonoge, strige;</strong></em></li>
<li style="text-align: justify;">- Skladišni štetnici: <em><strong>leptiri, moljci, kornjaši: žišci, crvotočci, kožojedi, slaninari, brašnari i gljivari, grinje</strong></em>.;</li>
<li style="text-align: justify;">- Štetnici estetske ili javnozdravstvene važnosti:<em><strong> skokuni, babure, uholaže i šturci.</strong></em> &nbsp; &nbsp;</li>
</ul>
<p style="text-align: justify;">&nbsp;</p>
<p style="text-align: justify;">&nbsp; U prehrambenoj industriji očekivane vrste insekata su:</p>
<ul>
<li style="text-align: justify;">&nbsp; -&nbsp; <em><strong>Crni žohar</strong></em> - Blatta orientalis</li>
<li style="text-align: justify;">&nbsp; -&nbsp; <em><strong>Smeđi žohar</strong></em>- Blattella&nbsp;germanica</li>
<li style="text-align: justify;">&nbsp; -&nbsp; <em><strong>Žuti mrav</strong></em> - Monomorium faranois &nbsp;</li>
<li style="text-align: justify;">&nbsp; -&nbsp; <em><strong>Domaća muha</strong></em> - Musca domestica &nbsp;</li>
<li style="text-align: justify;">&nbsp; -&nbsp; <em><strong>Žitni, kukuruzni, pirinčani&nbsp; žižak</strong></em> - Sitophilus sp &nbsp;</li>
<li style="text-align: justify;">&nbsp; -&nbsp; <em><strong>Kestenjasti brašnar</strong></em> - Tribolium castaneum &nbsp;</li>
<li style="text-align: justify;">&nbsp; -&nbsp; <em><strong>Mali brašnar</strong></em> - Cryptolestes sp &nbsp;</li>
<li style="text-align: justify;">&nbsp; -&nbsp; <em><strong>Smeđi brašnar</strong></em> - Rhizopertha dominica &nbsp;</li>
<li style="text-align: justify;">&nbsp; -&nbsp; <em><strong>Bakrenasti moljac</strong></em> - Plodia interpunctella &nbsp;</li>
</ul>
